<?php
class ModuleListView extends RecordListView
{
    public $module_config = null;
    public $m_process_url = null;
    public $module_name = null;

    function __construct()
    {
        parent::__construct();
        require_once $_SERVER["DOCUMENT_ROOT"].JOINT_SITE_EXEC_DIR.
            "/application/views/templates/ModuleMenu.php";
        $this->styles[] = JOINT_SITE_EXEC_DIR."/css/module.css";
    }

    function print_page_content()
    {
        $aliasMenu = null;
        if(isset($this->lang_map->menu_blocks["modules_menu"]["menu_items"][$this->module_name]["aliasMenu"])){
            $aliasMenu = $this->lang_map->menu_blocks["modules_menu"]["menu_items"][$this->module_name]["aliasMenu"];
        }
        echo moduleMenu::print_module_menu($aliasMenu, $this->module_config, $this->m_process_url);
        parent::print_page_content(); // TODO: Change the autogenerated stub
    }
}