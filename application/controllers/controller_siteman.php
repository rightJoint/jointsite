<?php
include "application/core/Records/RecordsController.php";
include "application/core/Module/ModuleRecordsController.php";

class Controller_Siteman extends ModuleRecordsController
{
    public $modules;

    function __construct()
    {
        $this->lang_map["menu-index-warning"] = array(
            "en" => "denied in controller siteman action index",
            "rus" => "Ошибка доступа в controller siteman action index",
        );

        $modulesInfo = null;
        require_once JOINT_CONF_DIR."/modulesInfo.php";

        $this->modules = $modulesInfo;

        parent::__construct();
    }

    function loadView($type_of_view, $process_path, $custom_name = null)
    {
        $loadedView = parent::loadView($type_of_view, $process_path, $custom_name); // TODO: Change the autogenerated stub

        $loadedView->robot_no_index = true;
        $loadedView->metrik_block = false;

        return $loadedView;
    }

    function action_sitemap()
    {
        global $routes;
        if($routes[3] == "update"){

            $this->model = $this->loadModel("siteman/sitemap", "update");
            $this->model->getRecordStructure();
            $view_data = $this->model->update_sitemap();
            include "application/views/siteman/sitemap/sitemapUpdateView.php";
            $this->view = new sitemapUpdateView();
            $this->view->module = $this->modules["sitemap"];
            $this->view->view_data = $view_data;
            $this->view->generate();
        }else{
            $this->module_process( "sitemap");
        }
    }

    function action_music()
    {
        $this->module_process("music");
    }

    function action_groups()
    {
        $this->module_process("groups");
    }

    function action_users()
    {
        $this->module_process("users");
    }

    function action_notifications()
    {
        $this->module_process("notifications");
    }

    function action_jointApi()
    {
        if($_GET["method"] == "fillDL"){
            if($_GET["custom_name"] == "musictracks"){
                if($_GET["album_id"]){
                    $req_where = json_decode($_GET["where"], true);
                    $search_tmp = $req_where["track_name"];
                    $this->fillMusicTracksList($search_tmp);
                }
            }
        }
        parent::action_jointApi();
    }
    /*used in music*/
    function fillMusicTracksList($search_tmp = null)
    {

        $this->view = new View();
        $list_qry = "select musicTracks_dt.track_name, musicTracks_dt.track_id from musicTracks_dt ".
            "left join musicTracksToAlb_dt on musicTracksToAlb_dt.track_id = musicTracks_dt.track_id ".
            "and musicTracksToAlb_dt.album_id = '".$_GET["album_id"]."' ".
            "where musicTracks_dt.track_name like '%".$search_tmp."%' ".
            "and musicTracksToAlb_dt.album_id is null";
        $this->model = new Model();
        $list_res = $this->model->query($list_qry);
        $list_return = array("" => "");
        if($list_res->rowCount()){
            while ($list_row = $list_res->fetch(PDO::FETCH_ASSOC)){
                $list_return[$list_row[$_GET["returnKey"]]] = $list_row[$_GET["findField"]];
            }
        }

        $this->view->generateJson($list_return);
        exit;

    }
}